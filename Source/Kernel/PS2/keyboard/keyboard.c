unsigned char ScancodeToKeyCode(unsigned char scancode, int e0, int prntscrn, int pause)
{
	if(pause == 1){
		return 0x10;
	}
	else if(prntscrn == 1)
		return 0x0E; 
	else if(e0 == 0)
		return ScancodeToKeyCode8[scancode];
	else if(e0 == 1)
		return ScancodeToKeyCode16[scancode];
	return 0x00;
}

unsigned char KeyCodeToASCII(unsigned char KeyCode, int isShift, int isAlt)
{
	if(isShift == 0 && isAlt == 0)
		return KeyCodeToASCII8[KeyCode];
	else if(isShift == 1 && isAlt == 0)
		return KeyCodeToASCIIShift8[KeyCode];
	else if(isShift == 0 && isAlt == 1)
		return KeyCodeToASCIIAlt8[KeyCode];
	else
		return 0x00;
}





