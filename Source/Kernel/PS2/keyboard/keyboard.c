unsigned char commandKeyboard(unsigned char byte)
{
	writeToFirstPS2Port(byte);
	controllerWait(0);
	return inb(0x60); 
}

unsigned char readKeyboard()
{
	controllerWait(0);
	return inb(0x60);
}

int initKeyboard()
{

	int try = 3;
	char res = commandKeyboard(0xF4);
	if(res == 0xFE)
	{
		while(res == 0xFE && try != 0)
		{
			res = commandKeyboard(0xF4);
			try--;
		}
	}
	if(res == 0xFC) return 0xFC;
	return 0xFA;
}


